package com.vernalis.nodes.io.pdb.loadlocal;

import org.knime.core.node.NodeDialogPane;
import org.knime.core.node.NodeFactory;
import org.knime.core.node.NodeView;


/**
 * <code>NodeFactory</code> for the "LocalPDBLoad" Node.
 * Load a local copy of a PDB files from a column of source filepaths or URLs
 */
public class LocalPDBLoadNodeFactory2 
        extends NodeFactory<LocalPDBLoadNodeModel2> {

    /**
     * {@inheritDoc}
     */
    @Override
    public LocalPDBLoadNodeModel2 createNodeModel() {
        return new LocalPDBLoadNodeModel2();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public int getNrNodeViews() {
        return 0;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeView<LocalPDBLoadNodeModel2> createNodeView(final int viewIndex,
            final LocalPDBLoadNodeModel2 nodeModel) {
        return null;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public boolean hasDialog() {
        return true;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeDialogPane createNodeDialogPane() {
        return new LocalPDBLoadNodeDialog2();
    }

}

