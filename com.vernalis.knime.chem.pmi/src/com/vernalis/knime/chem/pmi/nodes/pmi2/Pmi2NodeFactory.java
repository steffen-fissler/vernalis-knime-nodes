package com.vernalis.knime.chem.pmi.nodes.pmi2;

import org.knime.core.node.NodeDialogPane;
import org.knime.core.node.NodeFactory;
import org.knime.core.node.NodeView;

/**
 * <code>NodeFactory</code> for the "Pmi" Node. Node to calculate Principle
 * Moments of Inertia (PMI)
 *
 * @author Vernalis
 */
public class Pmi2NodeFactory extends NodeFactory<Pmi2NodeModel> {

	/**
	 * {@inheritDoc}
	 */
	@Override
	public Pmi2NodeModel createNodeModel() {
		return new Pmi2NodeModel();
	}

	/**
	 * {@inheritDoc}
	 */
	@Override
	public int getNrNodeViews() {
		return 0;
	}

	/**
	 * {@inheritDoc}
	 */
	@Override
	public NodeView<Pmi2NodeModel> createNodeView(final int viewIndex,
			final Pmi2NodeModel nodeModel) {
		return null;
	}

	/**
	 * {@inheritDoc}
	 */
	@Override
	public boolean hasDialog() {
		return true;
	}

	/**
	 * {@inheritDoc}
	 */
	@Override
	public NodeDialogPane createNodeDialogPane() {
		return new Pmi2NodeDialog();
	}

}
