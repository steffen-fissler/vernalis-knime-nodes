package com.vernalis.nodes.io.txt;

import org.knime.core.node.NodeDialogPane;
import org.knime.core.node.NodeFactory;
import org.knime.core.node.NodeView;

/**
 * <code>NodeFactory</code> for the "LoadTxt" Node.
 * Node to Load Agilent AEV files to a column in the table
 *
 * @author SDR
 */
public class LoadTxtNodeFactory 
        extends NodeFactory<LoadTxtNodeModel> {

    /**
     * {@inheritDoc}
     */
    @Override
    public LoadTxtNodeModel createNodeModel() {
        return new LoadTxtNodeModel();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public int getNrNodeViews() {
        return 0;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeView<LoadTxtNodeModel> createNodeView(final int viewIndex,
            final LoadTxtNodeModel nodeModel) {
        return new LoadTxtNodeView(nodeModel);
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public boolean hasDialog() {
        return true;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeDialogPane createNodeDialogPane() {
        return new LoadTxtNodeDialog();
    }

}

